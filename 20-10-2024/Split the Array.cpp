// User function template for C++
class Solution {
  public:
    int countgroup(vector<int>& arr) {
        // Complete the function
        int n =arr.size();
        int val =0;
        for(int i=1;i<n;i++){
            val^=arr[i];
        }
        if(val==arr[0]){
            return n;
        }
        return 0;
    }
};


